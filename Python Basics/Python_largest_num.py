# return maximum value from array using max(builtin Function)

def largest (arr,n):
    return(max(arr))

arr=[3,4,6,7,88]
n=len(arr)

#display max calling function here passing arr and its size/length.
ans=largest(arr,n)

print(f"Largest value is {ans}")


# return smallest value from array using min(builtin Function)
def smallest (arr,n):
    return(min(arr))

arr=[3,4,6,7,88]
n=len(arr)

#display max calling function here passing arr and its size/length.
ans=smallest(arr,n)

print(f"smallest value is {ans}")



